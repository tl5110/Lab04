package toys;

/**
 * An enumerated type for some common colors.
 *
 * To specify a color, e.g. BLUE:  Color.BLUE
 *
 * When displayed to standard output, the toString(): "BLUE"
 *
 * @author RIT CS
 */
public enum Color {
    RED,
    ORANGE,
    YELLOW,
    GREEN,
    BLUE,
    PURPLE,
    BROWN,
    WHITE,
    BLACK
}
